package com.dajie.wika.wap.model;

public class CorpOption {
	
	private String id;
	
	private String value;
	
	public CorpOption(String id,String value){
		this.id=id;
		this.value=value;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	

}
